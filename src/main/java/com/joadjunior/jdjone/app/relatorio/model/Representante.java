package com.joadjunior.jdjone.app.relatorio.model;

public class Representante {
	
	private String grc;
	private String ga;
	private String gc;
	
	public Representante() {
		super();
	}

	public Representante(String grc, String ga, String gc) {
		super();
		this.grc = grc;
		this.ga = ga;
		this.gc = gc;
	}

	public String getGrc() {
		return grc;
	}

	public void setGrc(String grc) {
		this.grc = grc;
	}

	public String getGa() {
		return ga;
	}

	public void setGa(String ga) {
		this.ga = ga;
	}

	public String getGc() {
		return gc;
	}

	public void setGc(String gc) {
		this.gc = gc;
	}

	
	
}
